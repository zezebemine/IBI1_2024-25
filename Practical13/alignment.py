#Compare two sequences using BLOSUM62 matrix and calculate alignment score, total score, and identity percentage.
def compare_sequences(seq1, seq2, blosum62):
    if len(seq1) != len(seq2):
        raise ValueError("Sequences must be of equal length for non-gapped alignment")
    
    scores = [] #store alignment score at a list
    identical_count = 0 #initialise identical score
    
    for aa1, aa2 in zip(seq1, seq2): #travel seq1 and seq2 and align the two sequences of proteins one by one to judeg whether they are the same.
        score = blosum62.get(aa1, {}).get(aa2, -4) # Default penalty of -4 for invalid AA
        scores.append(score) #store score to s=alignment_score
        #judeg whether aa1 equal aa2 or not to update identical score
        if aa1 == aa2: 
            identical_count += 1
    
    alignment_score = sum(scores) #calculate total score
    percent_identity = (identical_count / len(seq1)) * 100 #calculate identity percent
    
    return  alignment_score, percent_identity #output return value

